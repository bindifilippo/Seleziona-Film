package it.unito.server.model;

public class Genere {
    private String id;
    private String name;

    public Genere() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}